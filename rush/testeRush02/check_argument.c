int	check_argument(char *nbr)
{
	int	i;
	int	not_nbr;

	i = 0;
	not_nbr = 0;
	while (nbr[i])
	{
		if (nbr[i] < '0' || nbr[i] > '9')
			not_nbr = 1;
		i++;
	}
	if (i)
		return (not_nbr);
	return (1);
}
